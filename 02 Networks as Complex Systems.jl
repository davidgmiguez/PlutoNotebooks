```math 
A_{ij}  = A_{ji}, A_{ii} = 0
```
- and for directed networks 
```math 
A_{ij} \ne A_{ji}, A_{ii} = 0,
```
- and for directed networks with self-links
```math
A_{ij} \ne A_{ji}, A_{ii} \ne 0,
```





